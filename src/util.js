export function getLayout(scene) {
  const W = scene?.scale?.width || 1280;
  const H = scene?.scale?.height || 720;
  const vpW = typeof window !== 'undefined' ? (window.innerWidth || W) : W;
  const vpH = typeof window !== 'undefined' ? (window.innerHeight || H) : H;
  const short = Math.min(W, H);
  const compact = vpW < 920 || vpH < 700 || (vpW / Math.max(1, vpH)) < 1.4;
  const scale = Phaser.Math.Clamp(short / 720, 0.82, 1.25);
  const pad = Phaser.Math.Clamp(Math.round(short * 0.05), 16, 56);
  return {
    W,
    H,
    compact,
    scale,
    pad,
    panelW: compact ? Math.min(W - pad * 2, 560) : Math.min(560, Math.round(W * 0.42)),
    panelH: compact ? Math.min(H - pad * 2, 620) : Math.min(620, Math.round(H * 0.82))
  };
}

