export function clamp(value, min, max) {
  return Math.max(min, Math.min(max, value));
}

export function keyFor(path) {
  return String(path)
    .replace(/\.(png|jpg|jpeg)$/i, '')
    .replace(/[^a-z0-9]+/gi, '_')
    .replace(/^_+|_+$/g, '');
}

export function assetUrl(path) {
  return `${ASSET_ROOT}${path}`;
}

export function unique(list) {
  return [...new Set(list)];
}

export function loadManifest(scene) {
  ASSET_FILES.forEach((path) => {
    scene.load.image(keyFor(path), assetUrl(path));
  });
}

function animFrames(paths) {
  return paths.map((p) => ({ key: keyFor(p) }));
}

export function createAnim(game, key, paths, frameRate = 12, repeat = -1) {
  if (!paths || !paths.length) return;
  if (game.anims.exists(key)) return;
  game.anims.create({
    key,
    frames: animFrames(paths),
    frameRate,
    repeat,
    skipMissedFrames: true
  });
}

