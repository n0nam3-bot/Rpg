export function readControls(scene, controls) {
  const keys = scene.keys || {};
  const kb = {
    left: !!(keys.A && keys.A.isDown) || !!(keys.LEFT && keys.LEFT.isDown),
    right: !!(keys.D && keys.D.isDown) || !!(keys.RIGHT && keys.RIGHT.isDown),
    up: !!(keys.W && keys.W.isDown) || !!(keys.UP && keys.UP.isDown),
    down: !!(keys.S && keys.S.isDown) || !!(keys.DOWN && keys.DOWN.isDown),
    jump: !!(keys.SPACE && keys.SPACE.isDown) || !!(keys.W && keys.W.isDown) || !!(keys.UP && keys.UP.isDown),
    attack: !!(keys.X && keys.X.isDown) || !!(keys.ENTER && keys.ENTER.isDown),
    guard: !!(keys.SHIFT && keys.SHIFT.isDown),
    interact: !!(keys.E && keys.E.isDown),
    menu: !!(keys.ESC && keys.ESC.isDown)
  };
  const pad = controls && controls.state ? controls.state : {};
  return {
    left: kb.left || pad.left,
    right: kb.right || pad.right,
    up: kb.up,
    down: kb.down,
    jump: kb.jump || pad.jump,
    attack: kb.attack || pad.attack,
    guard: kb.guard || pad.guard,
    interact: kb.interact || pad.interact,
    menu: kb.menu || pad.menu
  };
}

