export function createTextButton(scene, x, y, w, h, label, onPress, opts = {}) {
  const fill = opts.fill || 0x2c1f38;
  const stroke = opts.stroke || 0xf0c6ff;
  const textColor = opts.textColor || '#ffffff';
  const group = scene.add.container(x, y).setScrollFactor(0).setDepth(opts.depth || 6000);
  const pad = Math.max(18, Math.round(Math.min(w, h) * 0.25));
  const hitW = w + pad * 2;
  const hitH = h + pad * 2;
  const hit = scene.add.rectangle(0, 0, hitW, hitH, 0x000000, 0.001).setOrigin(0.5);
  const rect = scene.add.rectangle(0, 0, w, h, fill, 0.96).setOrigin(0.5);
  rect.setStrokeStyle(2, stroke, 0.9);
  const text = scene.add.text(0, 0, label, {
    fontSize: opts.fontSize || '16px',
    color: textColor,
    fontStyle: 'bold',
    align: 'center',
    wordWrap: { width: Math.max(40, w - 18) }
  }).setOrigin(0.5);
  group.add([hit, rect, text]);
  const fire = () => { if (onPress) onPress(); };
  hit.setInteractive(new Phaser.Geom.Rectangle(-hitW / 2, -hitH / 2, hitW, hitH), Phaser.Geom.Rectangle.Contains, {
    useHandCursor: true
  });
  hit.on('pointerdown', () => {
    rect.setAlpha(0.88);
    fire();
  });
  hit.on('pointerup', () => rect.setAlpha(0.96));
  hit.on('pointerupoutside', () => rect.setAlpha(0.96));
  hit.on('pointerover', () => rect.setAlpha(1));
  hit.on('pointerout', () => rect.setAlpha(0.96));
  return { group, rect, text, hit, fire };
}

export function makeVirtualControls(scene, mode = 'explore') {
  const touch = scene.sys.game.device.input.touch || (typeof window !== 'undefined' && window.matchMedia && window.matchMedia('(pointer: coarse)').matches);
  const visible = touch && scene.state && scene.state.settings && scene.state.settings.mobileControls !== false;
  const largeButtons = !!(scene.state && scene.state.settings && scene.state.settings.largeButtons);
  const w = scene.scale.width;
  const h = scene.scale.height;
  const isCompact = w < 1100 || h < 680;
  const buttonScale = largeButtons ? (isCompact ? 1.28 : 1.16) : (isCompact ? 1.08 : 1.0);
  const bottom = Math.max(18, Math.round(h * 0.04));
  const side = Math.max(14, Math.round(w * 0.02));

  const state = {
    left: false,
    right: false,
    jump: false,
    attack: false,
    guard: false,
    interact: false,
    menu: false,
  };

  const container = scene.add.container(0, 0).setScrollFactor(0).setDepth(7000).setVisible(visible);
  const buttons = {};

  const makeBtn = (key, x, y, bw, bh, label) => {
    const rect = scene.add.rectangle(x, y, bw * buttonScale, bh * buttonScale, 0x221327, 0.78).setScrollFactor(0).setDepth(7001);
    rect.setStrokeStyle(2, 0xf4c8ff, 0.72);
    const txt = scene.add.text(x, y, label, {
      fontSize: key === 'menu' ? '14px' : (isCompact ? '11px' : '12px'),
      color: '#fff',
      fontStyle: 'bold',
      align: 'center'
    }).setOrigin(0.5).setScrollFactor(0).setDepth(7002);
    container.add([rect, txt]);

    const press = () => { state[key] = true; rect.setAlpha(0.96); };
    const release = () => { state[key] = false; rect.setAlpha(0.78); };
    rect.setInteractive(new Phaser.Geom.Rectangle(-(bw * buttonScale) / 2, -(bh * buttonScale) / 2, bw * buttonScale, bh * buttonScale), Phaser.Geom.Rectangle.Contains, { useHandCursor: true });
    rect.on('pointerdown', press);
    rect.on('pointerup', release);
    rect.on('pointerupoutside', release);
    rect.on('pointerout', release);
    buttons[key] = { rect, txt };
  };

  if (mode === 'battle') {
    if (isCompact) {
      const row1y = h - bottom - 160;
      const row2y = h - bottom - 58;
      makeBtn('left', side + 46, row1y, 88, 84, '◀');
      makeBtn('right', side + 146, row1y, 88, 84, '▶');
      makeBtn('guard', w - side - 268, row1y, 108, 84, 'GUARD');
      makeBtn('attack', w - side - 146, row1y, 108, 84, 'ATK');
      makeBtn('jump', side + 96, row2y, 136, 58, 'JUMP');
      makeBtn('menu', w - side - 80, row2y, 116, 52, 'RETREAT');
    } else {
      makeBtn('left', 112, h - 108, 92, 92, '◀');
      makeBtn('right', 216, h - 108, 92, 92, '▶');
      makeBtn('jump', w - 410, h - 108, 92, 92, 'JUMP');
      makeBtn('guard', w - 296, h - 108, 92, 92, 'GUARD');
      makeBtn('attack', w - 182, h - 108, 92, 92, 'ATK');
      makeBtn('menu', w - 82, 56, 124, 50, 'RETREAT');
    }
  } else {
    if (isCompact) {
      const row1y = h - bottom - 160;
      const row2y = h - bottom - 58;
      makeBtn('left', side + 46, row1y, 88, 84, '◀');
      makeBtn('right', side + 146, row1y, 88, 84, '▶');
      makeBtn('interact', w - side - 268, row1y, 108, 84, 'USE');
      makeBtn('attack', w - side - 146, row1y, 108, 84, 'ATK');
      makeBtn('jump', side + 96, row2y, 136, 58, 'JUMP');
      makeBtn('menu', w - side - 80, row2y, 116, 52, 'MENU');
    } else {
      makeBtn('left', 112, h - 108, 92, 92, '◀');
      makeBtn('right', 216, h - 108, 92, 92, '▶');
      makeBtn('jump', w - 410, h - 108, 92, 92, 'JUMP');
      makeBtn('interact', w - 296, h - 108, 92, 92, 'USE');
      makeBtn('attack', w - 182, h - 108, 92, 92, 'ATK');
      makeBtn('menu', w - 82, 56, 96, 48, 'MENU');
    }
  }

  return {
    visible,
    state,
    container,
    buttons,
    destroy() {
      container.destroy(true);
    },
    press(key) {
      if (state.hasOwnProperty(key)) state[key] = true;
    },
    release(key) {
      if (state.hasOwnProperty(key)) state[key] = false;
    }
  };
}

export function readControls(scene, controls) {
  const k = scene.keys || {};
  const c = controls ? controls.state : {};
  return {
    left: !!(k.left?.isDown || k.A?.isDown || c.left),
    right: !!(k.right?.isDown || k.D?.isDown || c.right),
    up: !!(k.up?.isDown || k.W?.isDown),
    down: !!(k.down?.isDown || k.S?.isDown),
    jump: !!(k.SPACE?.isDown || k.W?.isDown || c.jump),
    attack: !!(k.X?.isDown || k.J?.isDown || c.attack),
    guard: !!(k.SHIFT?.isDown || k.C?.isDown || c.guard),
    interact: !!(k.E?.isDown || k.ENTER?.isDown || c.interact),
    menu: !!(k.ESC?.isDown || c.menu)
  };
}

